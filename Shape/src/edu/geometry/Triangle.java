package edu.geometry;

public class Triangle {

    private double base;
    private double height;

    public double getBase() {
        return base;
    }

    private void setBase(double base) {
        this.base = base;
    }

    public double getHeight() {
        return height;
    }

    private void setHeight(double height) {
        this.height = height;
    }
}
